package com.example.agrimart.data.model;

public class Category {
    private String img;
    private String name;

    public Category() {
    }

    public Category(String img, String name) {
        this.img = img;
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}